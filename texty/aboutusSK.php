<h3>Oddelenia</h3>
<table>
    <tr>
        <td colspan="2"><h4>Oddelenie aplikovanej mechaniky a mechatroniky (OAMM)</h4></td>
    </tr><tr>
        <td>Vedúci:</td><td>prof. Ing. Justín Murín, DrSc.</td>
