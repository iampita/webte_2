<h3> History </h3>
<p> The Institute of Automobile Mechatronics was established on July 1, 2013 as a pedagogical and scientific research unit of the Faculty of Electrical Engineering and Computer Science of the STU in Bratislava. The establishment of the Institute of Automobile Mechatronics was a logical consequence of the intention of the Faculty of Electrical Engineering and Informatics of the STU in Bratislava to create an institute that would take into account the current requirements and needs of the automotive industry in Slovakia with the main aim of preparing bachelor and engineering graduates for automotive mechatronics.
        At present, the Institute of Automobile Mechatronics provides research, development and education in several areas of applied mechatronics with special emphasis in automotive mechatronics and mechatronic systems based on the integration and synergy of mechanical, electronic, information, communication and management technologies into complex mechatronic car systems. </p>

<p> The Institute guarantees study programs at all levels of study accredited at STU in Bratislava. For the wide-area teaching and research area, it integrates researchers and educators from the FEI STU into the research and teaching process in the individual study programs. </P>
