<P> The Department of Pedagogy provides in the bachelor's degree program the teaching of subjects with a major emphasis on mechanics and mechatronic elements. At the engineering level, ŠP ensures the teaching of subjects with emphasis on simulation and modeling of mechanical and mechatronic systems, both in terms of mechanics and dynamics, as well as from the point of view of multiphysical interconnection of individual physical domains. </P>

<P> The members of the department are concerned with the formulation of new mathematical procedures and methods used in multiphysical analyzes, (FGM), dynamic analyzes of mechatronic and MEMS systems as well as a description of piezoelectric elements. </P>

<P> The members of the department use modern SW tools such as ANSYS, Catia and MSC.ADAMS to design, analyze and optimize individual components as well as entire subsystems of mechatronic elements. </P>
